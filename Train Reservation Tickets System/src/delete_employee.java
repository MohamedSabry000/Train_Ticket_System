    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        String user = user_name.getText();
        char[] pas= pass.getPassword();
        String pa = String.copyValueOf(pas);
        int g=0 , f=0;
        g=user.length();
        f=pa.length();
        if(g==0 || f==0)
        {
            JOptionPane.showMessageDialog(this , "you must enter all required information ");
        }
        else{
            try {
                // TODO add your handling code here:
                PreparedStatement st = con.prepareStatement("delete from users " + " where user_name=? and user_password=? ");
                st.setString(1, user);
                st.setString(2, pa);

                PreparedStatement check = con.prepareStatement("SELECT *FROM users WHERE user_name=? and user_password=?");
                check.setString(1, user);
                check.setString(2, pa);
                ResultSet rs = check.executeQuery();

                if (rs.next()) {
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this , "you deleted successefuly ");
                    dispose();
                    delete_employee del=new delete_employee();
                    del.setVisible(false);
                } else {

                    user_name.setText("");
                    pass.setText("");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this , "please enter valid information ");
            }
        }
        
    }//GEN-LAST:event_jButton13ActionPerformed

